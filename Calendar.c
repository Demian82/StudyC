#include <stdio.h>

int main(void)
{
	int month, days;
	
	printf("일수를 알고 싶은 달을 입력하시요: ");
	scanf("%d", &month);
	
	switch (month)
	{
		case 2:
			days = 28;
			break;
		case 4 || 6 || 9 || 11:
			days = 30;
			break;
		default:
			days = 31;
			break;
	 } 
	printf("%d월의 일수는 %d입니다.\n", month, days);
	
	return 0;
}
