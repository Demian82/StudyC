#include <stdio.h>
#include <math.h>

int main(void)
{
	printf("sin : %lf\n", sin(3.14/2.0));
	printf("cos : %lf\n", sin(3.14/2.0));
	printf("tan : %lf\n", sin(0.5));
	printf("exp : %lf\n", exp(10.0));
	printf("log : %lf\n", log(10.0));
	printf("ceil : %lf\n", ceil(3.8));
	printf("floor : %lf\n", floor(3.8));
	printf("fabs : %lf\n", fabs(-3.8));
	printf("pow : %lf\n", pow(5, 5));
	printf("sqrt : %lf\n", sqrt(4.0));
	
	return 0;
}
